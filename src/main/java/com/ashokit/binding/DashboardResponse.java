package com.ashokit.binding;

public class DashboardResponse {
     private int totalEnq;
     private int enrolledEnq;
     private int lostEnq;
     
	public int getTotalEnq() {
		return totalEnq;
	}
	public void setTotalEnq(int totalEnq) {
		this.totalEnq = totalEnq;
	}
	public int getEnrolledEnq() {
		return enrolledEnq;
	}
	public void setEnrolledEnq(int enrolledEnq) {
		this.enrolledEnq = enrolledEnq;
	}
	public int getLostEnq() {
		return lostEnq;
	}
	public void setLostEnq(int lostEnq) {
		this.lostEnq = lostEnq;
	}
     
	
     
}
